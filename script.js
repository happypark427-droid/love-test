/* 3. 화면에 질문을 그려주는 함수 (가장 핵심적인 두뇌 역할) */
function showQuestion() {
  const q = questions[current]; /* 현재 번호에 맞는 질문 데이터를 가져옴 */

  /* 상단 진행도 표시 (예: 1 / 15) */
  document.getElementById("progress").innerText =
    `${current + 1} / ${questions.length}`;

  document.getElementById("question").innerText = q.q;
/* 질문 텍스트 삽입 */
  const btnBox = document.getElementById("buttons");
  btnBox.innerHTML = "";

  /* 만약 질문이 '복수 선택(multi)' 타입이라면? */
  if (q.multi) {
    q.a.forEach((text, i) => {
      /* 체크박스 형태로 선택지 생성 */
      const label = document.createElement("label");
      label.style.display = "block";
      label.style.margin = "15px";

      const checkbox = document.createElement("input");
      checkbox.type = "checkbox";
      checkbox.value = q.score[i]; /* 체크박스에 해당 점수를 심어둠 */
      checkbox.style.marginRight = "10px";

      label.appendChild(checkbox);
      label.appendChild(document.createTextNode(text));

      btnBox.appendChild(label);
    });

    /* [복원 기능] 이전에 이 질문에서 체크했던 기록이 있다면 다시 체크해줌 */
if (q.multi && answers[current]) {
  const checks = document.querySelectorAll("input[type='checkbox']");
  answers[current].forEach(i => {
    checks[i].checked = true;
  });
}
/* 복수 선택은 '다음' 버튼을 눌러야 넘어가도록 설정 */
    const nextBtn = document.createElement("button");
    nextBtn.innerText = "다음";
    nextBtn.onclick = nextMulti;
    btnBox.appendChild(nextBtn);

  } else {
    /* 단일 선택 질문인 경우: 버튼을 누르자마자 다음으로 넘어감 */
    q.a.forEach((text, i) => {
      const btn = document.createElement("button");
      btn.innerText = text;
      btn.onclick = () => answer(i); /* 클릭 시 answer 함수 실행 */
      btnBox.appendChild(btn);
    });
  }
/* [이전 버튼 제어] HTML에 이미 만들어둔 이전 버튼을 가져옴 */
  const prevBtn = document.getElementById("prev-button"); 
  
  if (current > 0) {
    prevBtn.classList.remove("hidden"); /* 0번 질문이 아니면 '이전으로' 버튼을 보여줌 */
  } else {
    prevBtn.classList.add("hidden");    /* 첫 번째 질문에서는 '이전으로' 버튼을 숨김 */
  }
}

/* 4. 이전 버튼 클릭 시 실행되는 기능 */
function prevQuestion() {
  if (current <= 0) return; /* 혹시나 첫 질문이면 아무 일도 안 함 */

 /* 번호를 하나 줄여서 과거로 돌아감 */
  current--;

  /* 방금 전까지 쌓였던 점수를 다시 빼야 함 (그래야 중복 합산이 안 됨) */
  const prevAnswer = answers[current];

  if (Array.isArray(prevAnswer)) {
    /* 복수 선택이었다면 체크했던 모든 항목의 점수를 차감 */
    prevAnswer.forEach(i => {
      score -= questions[current].score[i];
    });
  } else {
    /* 단일 선택이었다면 해당 점수 하나만 차감 */
    score -= questions[current].score[prevAnswer];
  }

  showQuestion(); /* 이전 질문 화면으로 다시 그림 */
}
 

/* 5. 복수 선택형 질문에서 '다음' 클릭 시 처리 */
function nextMulti() {
  const checks = document.querySelectorAll("input[type='checkbox']");
  let selected = [];

  /* 체크된 항목을 돌며 점수를 더하고 선택한 번호를 기록 */
  checks.forEach((c, i) => {
    if (c.checked) {
      score += parseInt(c.value);
      selected.push(i);
    }
  });

  answers[current] = selected; /* 선택 기록 저장 */
  current++; /* 다음 질문 번호로 이동 */ /* 모든 질문이 끝났으면 결과창을, 아니면 다음 질문을 보여줌 */
  current < questions.length ? showQuestion() : showResult();
}

