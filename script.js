/* 결과 화면을 이미지로 저장하는 함수 */
function saveResult() {
  /* 캡처할 대상 (결과 박스 전체) */
  const target = document.getElementById("result-box");

  /* 캡처 옵션 설정 */
  html2canvas(target, {
    useCORS: true,      /* 외부 이미지(카카오 등) 허용 */
    allowTaint: true,
    backgroundColor: "#ffffff", /* 배경색을 흰색으로 지정 */
    scale: 2            /* 숫자가 높을수록 고화질 (2~3 권장) */
  }).then(canvas => {
    /* 캡처된 결과물을 이미지 주소로 변환 */
    const image = canvas.toDataURL("image/jpeg", 0.9);
    
    /* 가짜 링크를 만들어 이미지를 다운로드 시킴 */
    const link = document.createElement("a");
    link.href = image;
    link.download = "연애력_테스트_결과.jpg"; /* 저장될 파일 이름 */
    link.click();
  });
}

