/* 10. 결과 화면을 이미지로 저장하는 함수 */
function saveResult() {
  const target = document.getElementById("result-box");
  if (!target) return;

  html2canvas(target, {
    useCORS: true,
    allowTaint: true,
    backgroundColor: "#ffffff",
    scale: 2
  }).then(canvas => {
    const image = canvas.toDataURL("image/jpeg", 0.9);
    const link = document.createElement("a");
    link.href = image;
    link.download = "연애력_테스트_결과.jpg";
    link.click();
  });
}

